package me.eliab.sbcontrol.enums;

/**
 * Determines the score number format type
 */
public enum NumberFormatType {

    /**
     * Show nothing.
     */
    BLANK,

    /**
     * The styling to be used when formatting the score number.
     * This is similar to a Chat, but only the styling fields are present.
     */
    STYLED,

    /**
     * The text to be used as placeholder.
     */
    FIXED

}
